//Question4_Get name, legs, color, age and bark value from the dog object.

const dog = {
    name: "Mex",
    legs: 4,
    color: "Brown",
    age: 3,
    bark: function() {
      console.log("Woof woof!");
    }
  };
  
  // Access the properties of the dog object
  const name = dog.name;
  const legs = dog.legs;
  const color = dog.color;
  const age = dog.age;
  
  // Call the bark method
  dog.bark();
  
  // Print the values
  console.log("Name: " + name);
  console.log("Legs: " + legs);
  console.log("Color: " + color);
  console.log("Age: " + age);
  





















































  