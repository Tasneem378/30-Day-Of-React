//Question1_Get user input using prompt(“Enter your age:”). If user is 18 or older , give
//feedback:'You are old enough to drive' but if not 18 give another feedback stating to wait
//for the number of years he needs to turn 18.

// Get user input for age
const userInput = prompt("Enter your age:");

// Convert the user input to a number
const userAge = parseInt(userInput);

// Check if the user is 18 or older
if (userAge >= 18) {
  console.log("You are old enough to drive.");
} else {
  const yearsToWait = 18 - userAge;
  console.log(`You are not old enough to drive. Please wait for ${yearsToWait} year to turn 18.`);
}






























































































































































